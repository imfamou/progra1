#--------------------OPERADORES RELACIONAIS-----------------------------

#Em Python, usamos operadores matemáticos (e lógicos) para comparar valores de objetos ou dados que queiramos usar numa CONDICIONAL
#Como se trata de uma linguagem de programação, existem conceitos lógicos que se aplicam automaticamente, entre eles:

#------------ A LÓGICA BOOLEANA
# Este tipo de lógica é usado por TODOS os computadores (e, consequentemente, linguagens de programação) porque só considera dois valores:
# True (Verdadeiro)
# False (Falso)

#Estes valores correspondem, respetivamente, ao 1 e ao 0 - CÓDIGO BINÁRIO
#Os computadores funcionam todos mediante esta lógica (com zeros e uns - é a sua linguagem).
#------------LISTA DOS OPERADORES--------------

# IGUAL A [==]
# usamos dois iguais (=) para ver se alguma coisa é IGUAL A outra de acordo com a linguagem do computador

# DIFERENTE DE [!=]
# usamos um ponto de exclamação [!] e um igual [=] para ver se alguma coisa é DIFERENTE DE outra coisa, ou seja, NÃO IGUAL, porque [!] = NÃO e [=] IGUAL

# MAIOR QUE [>]

# MAIOR OU IGUAL A [>=]

# MENOR QUE [<]

# MENOR OU IGUAL A [<=]
#-------------------CONDICIONAIS------------------------

#A maior parte da lógica de programação provém das CONDICIONAIS, que, por sua vez, também respeitam a lógica BOOLEANA.

#Em Python, uma estrutura condicional é representada da seguinte forma:

# if condição:
#    código
# else:
#    código ou resto do código

# ou seja, IF = SE; ELSE = SENÃO.
